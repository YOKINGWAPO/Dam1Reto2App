--  DDL for Trigger TRG_COMP_PREVENT_TIPO_CHANGE
--------------------------------------------------------

  CREATE OR REPLACE EDITIONABLE TRIGGER "CONEXION"."TRG_COMP_PREVENT_TIPO_CHANGE" 
BEFORE UPDATE OF tipo_componente, id_categoria
ON componentes
FOR EACH ROW
DECLARE
  v_cnt NUMBER;
BEGIN
  IF NVL(:old.tipo_componente,'?') = NVL(:new.tipo_componente,'?')
     AND NVL(:old.id_categoria,-1) = NVL(:new.id_categoria,-1) THEN
    RETURN;
  END IF;

  SELECT
      (SELECT COUNT(*) FROM cpu            WHERE id_componente = :old.id_componente)
    + (SELECT COUNT(*) FROM gpu            WHERE id_componente = :old.id_componente)
    + (SELECT COUNT(*) FROM ram            WHERE id_componente = :old.id_componente)
    + (SELECT COUNT(*) FROM almacenamiento WHERE id_componente = :old.id_componente)
    + (SELECT COUNT(*) FROM placa_base     WHERE id_componente = :old.id_componente)
    + (SELECT COUNT(*) FROM monitor        WHERE id_componente = :old.id_componente)
    + (SELECT COUNT(*) FROM cascos         WHERE id_componente = :old.id_componente)
    + (SELECT COUNT(*) FROM consola        WHERE id_componente = :old.id_componente)
    + (SELECT COUNT(*) FROM psu            WHERE id_componente = :old.id_componente)
    + (SELECT COUNT(*) FROM caja           WHERE id_componente = :old.id_componente)
  INTO v_cnt
  FROM dual;

  IF v_cnt > 0 THEN
    RAISE_APPLICATION_ERROR(-20012, 'No puedes cambiar tipo/categoría: el componente ya tiene especificaciones registradas.');
  END IF;
END;

/
