--------------------------------------------------------
--  DDL for Package Body PKG_PRODUCTOS
--------------------------------------------------------

  CREATE OR REPLACE EDITIONABLE PACKAGE BODY "CONEXION"."PKG_PRODUCTOS" AS
  PROCEDURE p_get_productos_categoria(
    p_categoria IN NUMBER,
    p_rc OUT SYS_REFCURSOR
  ) IS
  BEGIN
    OPEN p_rc FOR
      SELECT
        -- Datos generales
        c.id_componente,
        c.id_proveedor,
        c.id_categoria,
        c.id_marca,
        c.nombre,
        c.descripcion,
        c.tipo_componente,
        c.precio,
        c.stock,
        c.imagen,
        c.activo,
        c.fecha_alta,

        -- CPU specs
        cpu.socket   AS cpu_socket,
        cpu.nucleos  AS cpu_nucleos,
        cpu.hilos    AS cpu_hilos,
        cpu.tdp_w    AS cpu_tdp_w,

        -- GPU specs
        gpu.vram_gb    AS gpu_vram_gb,
        gpu.consumo_w  AS gpu_consumo_w,
        gpu.chipset    AS gpu_chipset,

        -- PLACA_BASE specs
        pb.socket    AS pb_socket,
        pb.chipset   AS pb_chipset,
        pb.formato   AS pb_formato,
        pb.ram_tipo  AS pb_ram_tipo,

        -- RAM specs
        ram.capacidad_gb    AS ram_capacidad_gb,
        ram.tipo            AS ram_tipo,
        ram.frecuencia_mhz  AS ram_frecuencia_mhz,

        -- ALMACENAMIENTO specs
        alm.tipo          AS alm_tipo,
        alm.capacidad_gb  AS alm_capacidad_gb,
        alm.interfaz      AS alm_interfaz,

        -- MONITOR specs
        mon.tamano_pulg  AS mon_tamano_pulg,
        mon.resolucion   AS mon_resolucion,
        mon.hz           AS mon_hz,
        mon.panel        AS mon_panel,

        -- CASCOS specs
        cas.conexion   AS cas_conexion,
        cas.microfono  AS cas_microfono,

        -- CONSOLA specs
        con.almacenamiento_gb AS con_almacenamiento_gb,

        -- PSU specs
        psu.potencia_w     AS psu_potencia_w,
        psu.certificacion  AS psu_certificacion,
        psu.modular        AS psu_modular,

        -- CAJA specs
        caja.formato_soportado AS caja_formato_soportado

      FROM componentes c

      -- joins por tipo
      LEFT JOIN cpu cpu
        ON cpu.id_componente = c.id_componente AND c.tipo_componente = 'CPU'

      LEFT JOIN gpu gpu
        ON gpu.id_componente = c.id_componente AND c.tipo_componente = 'GPU'

      LEFT JOIN placa_base pb
        ON pb.id_componente = c.id_componente AND c.tipo_componente = 'PLACA_BASE'

      LEFT JOIN ram ram
        ON ram.id_componente = c.id_componente AND c.tipo_componente = 'RAM'

      LEFT JOIN almacenamiento alm
        ON alm.id_componente = c.id_componente AND c.tipo_componente = 'ALMACENAMIENTO'

      LEFT JOIN monitor mon
        ON mon.id_componente = c.id_componente AND c.tipo_componente = 'MONITOR'

      LEFT JOIN cascos cas
        ON cas.id_componente = c.id_componente AND c.tipo_componente = 'CASCOS'

      LEFT JOIN consola con
        ON con.id_componente = c.id_componente AND c.tipo_componente = 'CONSOLA'

      LEFT JOIN psu psu
        ON psu.id_componente = c.id_componente AND c.tipo_componente = 'PSU'

      LEFT JOIN caja caja
        ON caja.id_componente = c.id_componente AND c.tipo_componente = 'CAJA'

      WHERE c.activo = 1
        AND c.id_categoria = p_categoria
      ORDER BY c.id_componente;

  END p_get_productos_categoria;
END pkg_productos;

